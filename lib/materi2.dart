// materi 2 : perulangan

main() {
  // Perulangan dengan for
  for (int i = 0; i < 10; i++) {
    print('Nilai i = $i');
  }

  // Contoh lain perulangan dengan for
  var nama = 'akungeblog.com';
  for (int x = 0; x < nama.length; x++) {
    print('Huruf ke $x adalah ${nama[x]}');
  }

  // Perulangan dengan do while, foreach, dll
}
